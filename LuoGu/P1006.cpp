int cache[52][52][52][52];

int cache_search(int a, int b, int x, int y) {
	if (cache[a][b][x][y] != -1) return cache[a][b][x][y];
	
	if (a == m - 1 && b == n - 1 && x == m - 1 && y == n - 1) return 0;
	
	int backup, maxn, cur;
	maxn = cur = (backup = mp[x][y]) + mp[a][b];
	mp[x][y] = -1;
	// 同时向右 
	if (a + 1 < m && x + 1 < m && mp[a + 1][b] != -1) 
		maxn = max(maxn, cur + cache_search(a + 1, b, x + 1, y));
	// 同时向下
	if (b + 1 < n && y + 1 < n && mp[a][b + 1] != -1) 
		maxn = max(maxn, cur + cache_search(a, b + 1, x, y + 1));
	// 这两种都需要判断移动后会不会同一个点，或者都到达了终点 
	// ab右。xy下 
	if (a + 1 < m && y + 1 < n && !(a + 1 == x && y + 1 == b) && mp[a + 1][b] != -1) 
		maxn = max(maxn, cur + cache_search(a + 1, b, x, y + 1));
	// ab下，xy右
	if (b + 1 < n && x + 1 < m && !(a == x + 1 && y == b + 1) && mp[a][b + 1] != -1) 
		maxn = max(maxn, cur + cache_search(a, b + 1, x + 1, y));
		
	mp[x][y] = backup; 
	cache[a][b][x][y] = maxn;
	return maxn;
}


